package org.opengroove.jzbot;

public class FactTimeExceededError extends Error
{
    
    public FactTimeExceededError()
    {
        super();
    }
    
    public FactTimeExceededError(String message, Throwable cause)
    {
        super(message, cause);
    }
    
    public FactTimeExceededError(String message)
    {
        super(message);
    }
    
    public FactTimeExceededError(Throwable cause)
    {
        super(cause);
    }
    
}
